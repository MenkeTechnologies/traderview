//! Multi-jurisdictional rental property AUTOMATIC GARAGE
//! DOOR OPENER safety compliance framework. When a
//! landlord rents a property with an automatic garage
//! door opener, what entrapment-protection standards
//! apply (UL 325 photoelectric eye + auto-reverse +
//! sensing edge), what pre-1993 legacy opener replacement
//! obligations attach, and what failure-mode liabilities
//! expose landlord after a child-entrapment injury or
//! fatality?
//!
//! Distinct from sibling modules: rental_window_blind_
//! cord_safety (iter 469 — child strangulation product
//! safety), rental_window_guard_installation (fall
//! protection), rental_swimming_pool_drain_safety (VGB
//! Act), rental_carbon_monoxide_detector, rental_chimney_
//! fireplace_inspection_disclosure (iter 471), rental_
//! fire_extinguisher_requirement (iter 473), rental_
//! hardwired_smoke_alarm_responsibility (iter 481),
//! rental_bedroom_egress_window (structural), tenant_
//! emotional_distress_damages.
//!
//! Three-jurisdiction framework:
//!
//! 1. FEDERAL/CPSC (universal floor) — Consumer Product
//!    Safety Improvement Act of 1990 (15 U.S.C. § 2056),
//!    16 C.F.R. Part 1211 (Safety Standard for Automatic
//!    Residential Garage Door Operators), and ANSI/UL 325
//!    mandatory standard since JANUARY 1, 1993. UL 325
//!    requires (a) PRIMARY entrapment protection via
//!    internal motor monitoring with auto-reverse on
//!    contact; (b) SECONDARY external entrapment
//!    protection via PHOTOELECTRIC EYES (infrared beam
//!    between door tracks within 6 inches of floor) OR
//!    SENSING EDGE pressure-sensitive contact strips.
//!    CPSC reports incidents of child entrapment deaths
//!    or injuries reduced to NEARLY ZERO for automated
//!    doors properly installed with external photo-eye
//!    protection since 1993.
//! 2. CALIFORNIA — Cal. Civ. Code § 1941.1 implied
//!    warranty of habitability includes garage door
//!    operability per Green v. Superior Court, 10 Cal.
//!    3d 616 (1974); Cal. Health & Safety Code § 19211
//!    (water heater earthquake strap analog precedent
//!    for retrofit obligations); Cal. Bus. & Prof. Code
//!    § 7026 contractor licensing for installation and
//!    repair by licensed garage door installer (C-61/D-28
//!    classification).
//! 3. DEFAULT — Common-law implied warranty of
//!    habitability per Hilder v. St. Peter, 478 A.2d
//!    202 (Vt. 1984); Green v. Superior Court, 10 Cal.
//!    3d 616 (1974); tort negligence + premises
//!    liability + product-recall enforcement under
//!    Consumer Product Safety Act 15 U.S.C. § 2068
//!    prohibited-acts when continuing to lease unit
//!    with recalled garage door opener.
//!
//! UL 325 (post-1993) safety requirements:
//! 1. EXTERNAL ENTRAPMENT PROTECTION via photoelectric
//!    infrared sensors (mounted 4-6 inches above floor
//!    on either side of door opening) OR sensing-edge
//!    contact strips (mounted on bottom of door panel)
//! 2. INTERNAL ENTRAPMENT MONITORING via motor strain
//!    detection — door reverses if obstruction detected
//!    during travel
//! 3. AUTO-REVERSE on contact with ground obstruction
//!    within 2 seconds + 3-second timeout
//! 4. 5.5-SECOND CLOSE TIME enforced — minimum door-
//!    motion time before close completes (preventing
//!    door from slamming shut on small bodies)
//! 5. ANNUAL TESTING recommended: place 2x4 piece of
//!    lumber flat on ground in door path; door should
//!    contact + auto-reverse without crushing
//!
//! Three opener generations:
//! 1. PRE-1993 — manufactured before January 1, 1993;
//!    NOT equipped with external entrapment protection;
//!    CPSC + manufacturers strongly recommend REPLACEMENT
//!    not repair; insurance carriers commonly deny
//!    coverage for pre-1993 opener incidents
//! 2. 1993-2010 — first-generation UL 325 compliant;
//!    photoelectric eye + auto-reverse; aging components
//!    may have intermittent failures
//! 3. POST-2010 — current UL 325 edition with enhanced
//!    safety features including motor cutoff after
//!    excessive operation cycles + battery backup for
//!    power-outage emergency operation (CA SB 969 (2018)
//!    requires battery backup for new installations
//!    in California for emergency egress)
//!
//! Universal five failure-mode liability framework:
//! 1. PRE-1993 OPENER STILL IN SERVICE → child-
//!    entrapment liability and insurance denial; manuf-
//!    acturers and CPSC strongly recommend replacement
//!    (not repair) because pre-1993 lacks external
//!    protection device
//! 2. PHOTOELECTRIC EYE OBSTRUCTED OR DISABLED → tenant
//!    or prior occupant disabled sensor; entrapment
//!    protection failure; landlord verify operability
//!    at start of tenancy
//! 3. AUTO-REVERSE FAILURE on 2x4 test → physical impact
//!    liability + product defect (warranty claim with
//!    manufacturer if recall exists)
//! 4. PINCH-POINT UNPROTECTED — spring-loaded sections
//!    can amputate fingers; tenants with young children
//!    have heightened claim exposure
//! 5. CA SB 969 BATTERY BACKUP MISSING on post-July-1-2019
//!    installation in California → fire-safety code
//!    violation + emergency-egress habitability claim
//!
//! Trader-landlord critical because (1) pre-1993 garage
//! door openers are among the highest-stakes legacy
//! safety-equipment compliance issues — child-entrapment
//! fatality settlements routinely exceed $5M; (2) UL 325
//! photoelectric eye disable/obstruction is leading
//! cause of post-1993 entrapment events; (3) annual
//! 2x4 test at start of tenancy is single most
//! important verification task; (4) California SB 969
//! battery-backup mandate (effective July 1, 2019) for
//! new installations creates retrofit obligation; (5)
//! CPSC recall enforcement under 15 U.S.C. § 2068
//! applies if landlord continues to lease unit with
//! recalled opener.
//!
//! Authority: Consumer Product Safety Improvement Act of
//! 1990, Pub. L. 101-608 (15 U.S.C. § 2056); 16 C.F.R.
//! Part 1211 (Safety Standard for Automatic Residential
//! Garage Door Operators); ANSI/UL 325 (Standard for
//! Safety for Door, Drapery, Gate, Louver, and Window
//! Operators and Systems); Consumer Product Safety Act
//! 15 U.S.C. § 2068 (prohibited acts); 16 C.F.R. § 1115
//! (substantial-product-hazard reporting); Cal. Civ.
//! Code § 1941.1 (California implied warranty); Cal.
//! Bus. & Prof. Code § 7026 (contractor licensing);
//! Cal. SB 969 (2018, effective July 1, 2019 battery
//! backup requirement); Green v. Superior Court, 10
//! Cal. 3d 616 (1974); Hilder v. St. Peter, 478 A.2d
//! 202 (Vt. 1984).

use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Copy, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum Jurisdiction {
    California,
    Federal,
    Default,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum OpenerGeneration {
    Pre1993NonCompliant,
    Era1993To2010FirstGen,
    Post2010CurrentGen,
    None,
}

#[derive(Debug, Clone, Deserialize, Serialize)]
pub struct Input {
    pub jurisdiction: Jurisdiction,
    pub opener_generation: OpenerGeneration,
    pub photoelectric_eye_operable: bool,
    pub auto_reverse_passes_2x4_test: bool,
    pub sensing_edge_operable: bool,
    pub installed_after_july_1_2019_california: bool,
    pub battery_backup_present: bool,
    pub child_entrapment_event_reported: bool,
    pub cpsc_recall_outstanding: bool,
    pub last_annual_safety_test_months_ago: u32,
}

#[derive(Debug, Clone, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum Severity {
    NotApplicable,
    Compliant,
    AnnualTestRecommended,
    BatteryBackupMissing,
    AutoReverseFailure,
    EntrapmentProtectionFailure,
    Pre1993LegacyOpener,
    CpscRecallEnforcement,
    ChildEntrapmentEvent,
}

#[derive(Debug, Clone, Deserialize, Serialize)]
pub struct Output {
    pub severity: Severity,
    pub jurisdiction_specific_actions: Vec<String>,
    pub notes: Vec<String>,
}

pub const ANNUAL_TEST_MAX_MONTHS: u32 = 12;

pub type RentalGarageDoorSafetyComplianceInput = Input;
pub type RentalGarageDoorSafetyComplianceResult = Output;

pub fn check(input: &Input) -> Output {
    let notes: Vec<String> = vec![
        "Three-jurisdiction framework: Federal/CPSC (universal floor — Consumer Product Safety Improvement Act of 1990 + 16 C.F.R. Part 1211 + ANSI/UL 325 mandatory standard since JANUARY 1, 1993 + CPSC reports child-entrapment incidents reduced to NEARLY ZERO with properly installed external photo-eye protection since 1993); California (Cal. Civ. Code § 1941.1 implied warranty per Green v. Superior Court, 10 Cal. 3d 616 (1974) + Cal. Bus. & Prof. Code § 7026 C-61/D-28 contractor licensing + Cal. SB 969 (2018) battery-backup requirement effective July 1, 2019); Default (common-law implied warranty per Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984) + tort negligence + premises liability + Consumer Product Safety Act 15 U.S.C. § 2068 prohibited acts when continuing to lease unit with recalled opener).".to_string(),
        "UL 325 (post-1993) safety requirements: (1) EXTERNAL ENTRAPMENT PROTECTION via PHOTOELECTRIC INFRARED SENSORS (mounted 4-6 inches above floor) OR SENSING-EDGE contact strips; (2) INTERNAL ENTRAPMENT MONITORING via motor strain detection; (3) AUTO-REVERSE on contact within 2 seconds plus 3-second timeout; (4) 5.5-SECOND CLOSE TIME enforced minimum; (5) ANNUAL TESTING recommended via 2x4 lumber test (door should auto-reverse without crushing).".to_string(),
        "Three opener generations: (1) PRE-1993 NOT equipped with external entrapment protection — CPSC + manufacturers strongly recommend REPLACEMENT not repair; insurance carriers commonly DENY COVERAGE for pre-1993 opener incidents. (2) 1993-2010 first-generation UL 325 compliant; photoelectric eye + auto-reverse; aging components may have intermittent failures. (3) POST-2010 current UL 325 edition with enhanced features including motor cutoff after excessive cycles + battery backup (CA SB 969 effective July 1, 2019 requires battery backup for new installations in California for emergency egress).".to_string(),
        "Five universal failure-mode liabilities: (1) PRE-1993 OPENER STILL IN SERVICE → child-entrapment liability + insurance denial; (2) PHOTOELECTRIC EYE OBSTRUCTED OR DISABLED by tenant/prior occupant → entrapment protection failure; (3) AUTO-REVERSE FAILURE on 2x4 lumber test → physical impact liability + product defect; (4) PINCH-POINT UNPROTECTED on spring-loaded sections → finger amputation liability (heightened with young-children tenants); (5) CA SB 969 BATTERY BACKUP MISSING on post-July-1-2019 installation in California → fire-safety code violation + emergency-egress habitability claim.".to_string(),
        "Child-entrapment fatality settlements routinely exceed $5M; pre-1993 opener replacement cost $300-$800 vs litigation exposure. Annual 2x4 test at start of tenancy is single most important verification task. CPSC recall enforcement under 15 U.S.C. § 2068 prohibited-acts applies if landlord continues to lease unit with recalled opener.".to_string(),
        "Companion modules: rental_window_blind_cord_safety (iter 469 — child strangulation product safety), rental_window_guard_installation (fall protection), rental_swimming_pool_drain_safety (VGB Act), rental_carbon_monoxide_detector, rental_chimney_fireplace_inspection_disclosure (iter 471), rental_fire_extinguisher_requirement (iter 473), rental_hardwired_smoke_alarm_responsibility (iter 481), tenant_emotional_distress_damages.".to_string(),
    ];
    let mut actions: Vec<String> = Vec::new();

    if matches!(input.opener_generation, OpenerGeneration::None) {
        let mut n = notes;
        n.push("No automatic garage door opener present — UL 325 framework not applicable.".to_string());
        return Output {
            severity: Severity::NotApplicable,
            jurisdiction_specific_actions: actions,
            notes: n,
        };
    }

    if input.child_entrapment_event_reported {
        actions.push("Child-entrapment event reported: engage emergency services + counsel; preserve evidence including opener unit + photoelectric sensors + door panel; tort negligence + wrongful death + IIED exposure parallel to tenant_emotional_distress_damages iter 453; $5M+ settlement routine.".to_string());
    }

    if input.cpsc_recall_outstanding {
        actions.push("CPSC recall outstanding for installed opener: 15 U.S.C. § 2068 prohibited acts to continue leasing unit with recalled product; 16 C.F.R. § 1115 substantial-product-hazard reporting may apply; immediate retrofit + tenant notice required; coordinate with CPSC recall remedy program.".to_string());
    }

    if matches!(input.opener_generation, OpenerGeneration::Pre1993NonCompliant) {
        actions.push("PRE-1993 garage door opener still in service: CPSC + manufacturers strongly recommend REPLACEMENT (not repair) because pre-1993 lacks external photoelectric entrapment protection; child-entrapment liability exposure; insurance carriers commonly deny coverage; immediate replacement required.".to_string());
    }

    let post_1993 = matches!(
        input.opener_generation,
        OpenerGeneration::Era1993To2010FirstGen | OpenerGeneration::Post2010CurrentGen
    );

    let entrapment_failure = post_1993
        && !input.photoelectric_eye_operable
        && !input.sensing_edge_operable;
    if entrapment_failure {
        actions.push("UL 325 external entrapment protection FAILURE: photoelectric eye and sensing edge both inoperable; landlord must restore at least one functional external entrapment-protection device immediately; child-entrapment liability exposure significantly elevated.".to_string());
    }

    let auto_reverse_failure = post_1993 && !input.auto_reverse_passes_2x4_test;
    if auto_reverse_failure {
        actions.push("Auto-reverse 2x4 test FAILURE: door does not reverse on contact with obstruction; UL 325 internal entrapment monitoring violation; physical impact + crushing liability; engage licensed C-61/D-28 garage door installer for repair or replacement.".to_string());
    }

    let ca_battery_backup_violation = matches!(input.jurisdiction, Jurisdiction::California)
        && input.installed_after_july_1_2019_california
        && !input.battery_backup_present;
    if ca_battery_backup_violation {
        actions.push("California SB 969 (2018) battery-backup requirement violation: opener installed after July 1, 2019 in California must have battery backup for emergency egress during power outages; fire-safety code violation + emergency-egress habitability claim.".to_string());
    }

    let annual_test_overdue = post_1993
        && input.last_annual_safety_test_months_ago > ANNUAL_TEST_MAX_MONTHS;
    if annual_test_overdue {
        actions.push(format!(
            "Annual safety test overdue: {} months since last 2x4 lumber test exceeds 12-month best-practice interval; conduct test at start of every tenancy + annually thereafter.",
            input.last_annual_safety_test_months_ago
        ));
    }

    match input.jurisdiction {
        Jurisdiction::California => {
            actions.push("California: Cal. Civ. Code § 1941.1 implied warranty includes garage door operability + Cal. Bus. & Prof. Code § 7026 contractor licensing for installation (C-61/D-28 classification) + Cal. SB 969 (2018) battery-backup requirement for new installations after July 1, 2019.".to_string());
        }
        Jurisdiction::Federal => {
            actions.push("Federal: Consumer Product Safety Improvement Act of 1990 + 16 C.F.R. Part 1211 + ANSI/UL 325 mandatory since January 1, 1993; CPSC + manufacturers recommend replacement of pre-1993 openers.".to_string());
        }
        Jurisdiction::Default => {
            actions.push("Default jurisdiction: ANSI/UL 325 mandatory federal standard since January 1, 1993 + common-law implied warranty of habitability per Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984) + tort negligence + premises liability.".to_string());
        }
    }

    let severity = if input.child_entrapment_event_reported {
        Severity::ChildEntrapmentEvent
    } else if input.cpsc_recall_outstanding {
        Severity::CpscRecallEnforcement
    } else if matches!(input.opener_generation, OpenerGeneration::Pre1993NonCompliant) {
        Severity::Pre1993LegacyOpener
    } else if entrapment_failure {
        Severity::EntrapmentProtectionFailure
    } else if auto_reverse_failure {
        Severity::AutoReverseFailure
    } else if ca_battery_backup_violation {
        Severity::BatteryBackupMissing
    } else if annual_test_overdue {
        Severity::AnnualTestRecommended
    } else {
        Severity::Compliant
    };

    Output {
        severity,
        jurisdiction_specific_actions: actions,
        notes,
    }
}

