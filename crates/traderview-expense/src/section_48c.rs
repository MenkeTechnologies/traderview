//! IRC § 48C — Qualifying Advanced Energy
//! Project Credit Compliance Module.
//! Pure-compute check for ARRA 2009 and IRA 2022 § 13501
//! competitive-allocation tax-credit framework covering
//! clean-energy manufacturing, industrial decarbonization,
//! and critical-materials processing.
//!
//! **Three-phase legislative pattern**:
//! - **American Recovery and Reinvestment Act of 2009
//!   (ARRA 2009)**: § 1302 of Public Law 111-5 enacted
//!   IRC § 48C with an initial $2.3 billion allocation
//!   competitive-selection program;
//! - **Inflation Reduction Act of 2022**: § 13501 of
//!   Public Law 117-169 (enacted August 16, 2022) expanded
//!   § 48C with **$10 BILLION** additional allocation
//!   under new § 48C(e) Qualifying Advanced Energy
//!   Project Credit Allocation Program;
//! - **Two-Round Implementation**: **Round 1** allocated
//!   **$4 BILLION** in March 2024 (~$1.6B for energy
//!   communities); **Round 2** allocated **$6 BILLION**
//!   on January 10, 2025 for **APPROXIMATELY 140
//!   PROJECTS in approximately 30 STATES** (~$2.5B for
//!   energy communities).
//!
//! **Distinctive § 48C features**: **COMPETITIVE
//! ALLOCATION** program — NOT first-come-first-served;
//! allocations from a CAPPED POOL ($10 billion total);
//! **6% BASE rate** without PWA; **30% RATE with PWA**
//! (Prevailing Wage + Apprenticeship — 5x bump-up
//! multiplier identical to other IRA 2022 clean-energy
//! credits); **THREE PROJECT CATEGORIES** under § 48C(c):
//! clean-energy manufacturing/recycling, industrial
//! decarbonization (≥ 20% GHG reduction), critical
//! materials processing/refining/recycling; **40% ENERGY
//! COMMUNITIES RESERVATION** under § 48C(e)(3)(A) (closed
//! coal mines/plants); **2 YEARS from acceptance** to
//! provide certification evidence; **2 YEARS from
//! certification issuance** to place project in service;
//! eligible for **§ 6417 direct pay** (one of 12
//! § 6417(b) categories — listed as #11) and eligible for
//! **§ 38 GBC aggregation**.
//!
//! Web research (verified 2026-06-03):
//! - **American Recovery and Reinvestment Act of 2009 enactment**: § 1302 of Public Law 111-5 enacted IRC § 48C with initial $2.3 billion competitive-allocation program ([Cornell LII — 26 U.S. Code § 48C](https://www.law.cornell.edu/uscode/text/26/48C); [Bloomberg Tax — Sec. 48C Qualifying Advanced Energy Project Credit](https://irc.bloombergtax.com/public/uscode/doc/irc/section_48c); [House.gov — 26 USC 48C Qualifying Advanced Energy Project Credit](https://uscode.house.gov/view.xhtml?req=%28title%3A26+section%3A48C+edition%3Aprelim%29); [DOE — Qualifying Advanced Energy Project Credit (48C) Program](https://www.energy.gov/infrastructure/qualifying-advanced-energy-project-credit-48c-program); [DOE — 48C Qualifying Advanced Energy Project Credit FAQ PDF](https://www.energy.gov/sites/prod/files/edg/recovery/documents/Frequently_Asked_Questions_48C.pdf); [IRS — Frequently Asked Questions about Qualifying Advanced Energy Project (48C) Credit](https://www.irs.gov/credits-deductions/frequently-asked-questions-about-the-qualifying-advanced-energy-project-48c-credit); [IRS — Advanced Energy Project Credit](https://www.irs.gov/credits-deductions/businesses/advanced-energy-project-credit); [IRS — Qualifying Advanced Energy Project Credit Newsroom](https://www.irs.gov/newsroom/qualifying-advanced-energy-project-credit); [IRS Newsroom — IRS and Treasury Provide Guidance on Qualifying Advanced Energy Project Credit](https://www.irs.gov/newsroom/irs-and-treasury-provide-guidance-on-the-qualifying-advanced-energy-project-credit); [IRA Tracker — IRA Section 13501 Advanced Energy Project Credit](https://iratracker.org/programs/ira-section-13501-advanced-energy-project-credit/); [Honigman — IRS Opens Round Two of Qualifying Advanced Energy Project Credit Program with $6 Billion](https://www.honigman.com/alert-2645); [Paul Hastings — IRS Provides Initial Guidance on Code Section 48C Qualifying Advanced Energy](https://www.paulhastings.com/insights/client-alerts/irs-provides-initial-guidance-on-code-section-48c-qualifying-advanced-energy); [Tax Notes — Additional Guidance Issued on Energy Credit Allocations](https://www.taxnotes.com/research/federal/irs-guidance/notices/additional-guidance-issued-energy-credit-allocations/7jgqn); [Climate Program Portal — IRS Notice 2023-44 PDF](https://climateprogramportal.org/wp-content/uploads/2025/02/P_misc14.pdf); [McGuireWoods — IRS Issues More Guidance for Section 48C Advanced Energy Project Tax Credit Allocation Program](https://www.mcguirewoods.com/client-resources/alerts/2023/6/irs-issues-more-guidance-for-section-48c-advanced-energy-project-tax-credit-allocation-program/)).
//! - **Inflation Reduction Act of 2022 § 13501**: enacted August 16, 2022; provided **$10 BILLION** in funding for the expanded § 48C(e) Qualifying Advanced Energy Project Credit Allocation Program; Treasury allocated $10 billion in two rounds: $4 billion (Round 1) and $6 billion (Round 2); **40%** of funding reserved for projects in **ENERGY COMMUNITIES** with closed coal mines or coal plants.
//! - **§ 48C(a) Credit Amount — 6% Base + 30% PWA**: as modified by the IRA, Code Section 48C allows a taxpayer to claim the Section 48C Credit for any taxable year in an amount equal to a **BASE RATE OF 6%** of the taxpayer's **QUALIFIED INVESTMENT** for that taxable year with respect to any **QUALIFYING ADVANCED ENERGY PROJECT** owned by the taxpayer; the program provides an **INVESTMENT TAX CREDIT OF UP TO 30%** of qualified investments for certified projects that meet **PREVAILING WAGE AND APPRENTICESHIP REQUIREMENTS**.
//! - **§ 48C(c) Qualifying Advanced Energy Project — Three Categories**: a qualifying advanced energy project means a project, any portion of the qualified investment of which is certified by the Secretary under subsection (e) as eligible for a credit, which falls within one of **THREE CATEGORIES**: (1) **CLEAN ENERGY MANUFACTURING AND RECYCLING** — re-equips, expands or establishes an industrial or manufacturing facility to **PRODUCE OR RECYCLE SPECIFIED ADVANCED ENERGY PROPERTY**; (2) **INDUSTRIAL DECARBONIZATION** — installs technology in an industrial or manufacturing facility to **REDUCE GREENHOUSE GAS EMISSIONS BY AT LEAST 20%**; (3) **CRITICAL MATERIALS PROCESSING** — re-equips, expands or establishes an industrial facility to **PROCESS, REFINE OR RECYCLE CRITICAL MATERIALS**.
//! - **§ 48C(e)(3)(A) Energy Communities — 40% Reservation**: **40% OF FUNDING** reserved for projects in **ENERGY COMMUNITIES CENSUS TRACTS** with closed coal mines or coal plants; in Round 1 (March 2024): approximately **$1.6 BILLION** of the $4 billion allocation set aside for energy communities; in Round 2 (January 10, 2025): approximately **$2.5 BILLION** of the $6 billion allocation allocated to projects in designated energy communities.
//! - **§ 48C(e) Certification Timing — 2 Years from Acceptance**: each applicant for certification shall have **2 YEARS** from the date of acceptance by the Secretary of the application during which to provide evidence of meeting certification requirements.
//! - **§ 48C(e) Place in Service Timing — 2 Years from Certification**: an applicant has **2 YEARS** from the date of issuance of the certification to **PLACE THE PROJECT IN SERVICE**; if the taxpayer has placed the project in service within the required 2-year period and has notified DOE, the taxpayer may claim the § 48C credit on its income tax return for the taxable year in which the project was placed in service.
//! - **§ 48C(e) Round 1 Allocation — March 2024**: Treasury announced up to **$4 BILLION** in a first round of tax credits for projects that expand clean energy manufacturing and recycling and critical materials refining, processing and recycling, and for projects that reduce greenhouse gas emissions at industrial facilities.
//! - **§ 48C(e) Round 2 Allocation — January 10, 2025**: IRS allocated approximately **$6 BILLION** of the 48C credits for over **140 PROJECTS in approximately 30 STATES**, with approximately **$2.5 BILLION** allocated to projects in designated energy communities.
//! - **§ 48C eligible for § 6417 Direct Pay**: § 48C is one of the **12 APPLICABLE CREDIT CATEGORIES** under § 6417(b) for direct pay election by applicable entities (tax-exempt organizations + states + TVA + Indian tribal governments + Alaska Native Corporations + rural electric cooperatives).
//! - **§ 48C NOT eligible for § 6418 Transferability**: § 48C is **NOT** listed among the 11 eligible credits under § 6418(f)(1) — § 48C is the ONE CREDIT eligible for § 6417 but NOT for § 6418 (asymmetric monetization framework).

