//! IRC § 67(g) — Suspension of miscellaneous itemized
//! deductions. Added by Tax Cuts and Jobs Act of 2017
//! § 11045 (Pub. L. 115-97, enacted December 22, 2017);
//! originally scheduled to sunset after December 31,
//! 2025; **One Big Beautiful Bill Act of 2025 (OBBBA, H.R.
//! 1, signed July 4, 2025) made § 67(g) PERMANENT**.
//!
//! Trader-critical because § 67(g) is the single most
//! important reason traders ELECT trader status under
//! § 475(f) — without trader status, investment expenses
//! (advisory fees, custody fees, subscription fees, home
//! office, trading platform fees, education, travel)
//! become NON-DEDUCTIBLE under § 67(g) because they are
//! § 212 expenses subject to the suspended 2%-of-AGI floor.
//! With trader status under § 475(f), expenses qualify as
//! § 162 trade-or-business deductions on Schedule C and
//! ESCAPE § 67(g) suspension entirely.
//!
//! Completes the trader-status framework cluster:
//! section_475 (trader mark-to-market election);
//! section_162 (trade or business expense deductions);
//! section_212 (production-of-income expense deductions
//! suspended by § 67(g)); section_67 (2%-of-AGI floor on
//! misc itemized — now permanently suspended);
//! section_67e (estates and trusts carveout —
//! administration expenses STILL deductible per Rev. Rul.
//! 1995-26 + Treas. Reg. § 1.67-4 + IRS Notice 2018-61).
//!
//! Companion to section_469 (passive activity loss
//! rules), section_183 (hobby-loss rules — IRS pushback
//! against non-trader-status individuals claiming § 162),
//! section_1411 (NIIT 3.8% surtax — applies to § 212
//! investment income but exempts § 162 trader business
//! income).
//!
//! Trader-critical fact patterns:
//! - **Investor without § 475(f) trader status**: ALL
//!   investment-related expenses NON-DEDUCTIBLE under
//!   § 67(g) — including investment advisory fees, custody
//!   fees, subscription fees, financial publications,
//!   travel to investment seminars, home office, trading
//!   platform fees.
//! - **Trader with § 475(f) trader status**: SAME
//!   expenses fully deductible as § 162 trade-or-business
//!   on Schedule C; ESCAPES § 67(g) suspension; ALSO
//!   exempt from § 1411 NIIT.
//! - **Hedge fund LP investor** (not trader-status fund):
//!   K-1 § 212 expenses passed through as miscellaneous
//!   itemized deductions → NON-DEDUCTIBLE under § 67(g).
//! - **Hedge fund LP investor (trader-status fund)**: K-1
//!   § 162 expenses passed through as Schedule C
//!   ordinary deductions → fully deductible.
//! - **Estates and trusts**: § 67(e) carveout preserves
//!   deductibility of administration expenses (Treas. Reg.
//!   § 1.67-4 + IRS Notice 2018-61).
//!
//! **§ 67(g) Operative provision** — notwithstanding
//! subsection (a), no MISCELLANEOUS ITEMIZED DEDUCTION
//! shall be allowed for any taxable year beginning AFTER
//! DECEMBER 31, 2017. PERMANENT after OBBBA 2025.
//!
//! **§ 67(b) Items NOT treated as miscellaneous itemized
//! deductions** (exempted from § 67(g) suspension):
//! 1. § 67(b)(1) — § 163 interest;
//! 2. § 67(b)(2) — § 164 taxes;
//! 3. § 67(b)(3) — § 165(a) casualty losses (excluding
//!    misc casualty);
//! 4. § 67(b)(4) — § 170 charitable contributions;
//! 5. § 67(b)(5) — § 213 medical expenses;
//! 6. § 67(b)(6) — § 691(c) IRD deductions;
//! 7. § 67(b)(7) — § 215 alimony (pre-2019 divorces);
//! 8. § 67(b)(8) — § 217 moving expenses (members of armed
//!    forces only post-TCJA);
//! 9. § 67(b)(9) — § 1341 claim of right;
//! 10. § 67(b)(10) — gambling losses;
//! 11. § 67(b)(11) — § 642(c) charitable deductions for
//!     estates/trusts;
//! 12. § 67(b)(12) — § 7702B(a)(2) qualified long-term
//!     care insurance.
//!
//! **§ 67(e) Estates and trusts carveout** — § 67(g)
//! suspension does NOT apply to:
//! 1. Costs paid or incurred in connection with
//!    administration of estate or trust which would NOT
//!    have been incurred if property were not held in
//!    such trust or estate (TRUST/ESTATE-SPECIFIC
//!    expenses);
//! 2. Distributions to or for the benefit of beneficiary
//!    (§ 651, § 661, § 662);
//! 3. Distributable net income computations.
//!
//! Treas. Reg. § 1.67-4 + IRS Notice 2018-61 confirm
//! administration expenses survive § 67(g) for estates +
//! non-grantor trusts.
//!
//! **Items previously deductible under pre-TCJA § 67(a)
//! NOW SUSPENDED**:
//! - Unreimbursed employee business expenses (Form 2106)
//! - Investment advisory fees
//! - Investment custody/safekeeping fees
//! - Financial publication subscriptions
//! - Investment-related professional fees (CPA, attorney)
//! - Home office expenses (employee or investor)
//! - Trading platform subscriptions (non-trader status)
//! - Travel to investment seminars
//! - Tax-return preparation fees (individual)
//! - Hobby losses
//! - Safe deposit box fees
//! - § 212(1) production-of-income expenses
//! - § 212(2) management/conservation/maintenance of
//!   investment property
//! - § 212(3) tax determination/collection expenses
//!
//! **§ 475(f) trader election** — single most important
//! escape from § 67(g) suspension; converts § 212
//! investment expenses into § 162 business expenses on
//! Schedule C; ALSO converts § 1411 NIIT-includible
//! investment income into self-employment-tax-excluded
//! trader business income; ALSO permits MTM ordinary
//! treatment of trading gains/losses.
//!
//! Citations: 26 USC § 67(a)-(g); Tax Cuts and Jobs Act
//! of 2017 § 11045 (Pub. L. 115-97, December 22, 2017);
//! One Big Beautiful Bill Act of 2025 (H.R. 1, signed
//! July 4, 2025 — made § 67(g) PERMANENT); 26 USC § 162;
//! 26 USC § 212; 26 USC § 67(b)(1)-(12); 26 USC § 67(e);
//! 26 USC § 475(f); 26 USC § 1411; Treas. Reg. § 1.67-4;
//! IRS Notice 2018-61.

use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Copy, Deserialize, Serialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum TaxpayerStatus {
    /// Individual investor without § 475(f) trader
    /// status — § 67(g) suspension applies to all § 212
    /// expenses.
    InvestorNoTraderStatus,
    /// Individual with § 475(f) trader election in
    /// effect — § 162 trade-or-business deductions on
    /// Schedule C; ESCAPES § 67(g) suspension.
    TraderWithSection475fElection,
    /// Estate or non-grantor trust — § 67(e) carveout
    /// preserves administration expense deductibility.
    EstateOrNonGrantorTrust,
    /// Limited partner in hedge fund without trader
    /// status — K-1 § 212 expenses pass through as
    /// suspended misc itemized.
    HedgeFundLpInvestorFund,
    /// Limited partner in hedge fund WITH trader status —
    /// K-1 § 162 expenses fully deductible on Schedule C.
    HedgeFundLpTraderFund,
}

#[derive(Debug, Clone, Copy, Deserialize, Serialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum ExpenseCategory {
    /// § 212 investment expenses (advisory, custody,
    /// subscription, etc.) — suspended by § 67(g) for
    /// non-trader individuals.
    Section212InvestmentExpense,
    /// § 162 trade or business expenses (Schedule C) —
    /// NOT subject to § 67(g) suspension.
    Section162TradeOrBusinessExpense,
    /// § 67(b) exempt items (interest, taxes, charity,
    /// medical, etc.) — NOT suspended.
    Section67bExemptItem,
    /// § 67(e) trust/estate-specific administration
    /// expense — preserved by carveout.
    Section67eEstateAdminExpense,
    /// Unreimbursed employee business expense (Form 2106)
    /// — suspended by § 67(g).
    UnreimbursedEmployeeBusinessExpense,
    /// Tax-return preparation fee (individual) —
    /// suspended by § 67(g).
    TaxReturnPreparationFee,
    /// Hobby loss (§ 183) — suspended by § 67(g).
    HobbyLoss,
}

#[derive(Debug, Clone, Deserialize)]
pub struct Section67gInput {
    pub taxpayer_status: TaxpayerStatus,
    pub expense_category: ExpenseCategory,
    /// Expense amount in cents.
    pub expense_amount_cents: u64,
    /// Tax year of determination (TCJA effective 2018+;
    /// OBBBA made § 67(g) permanent after 2025).
    pub tax_year: u32,
    /// Taxpayer's AGI for 2%-floor calculation (pre-TCJA
    /// pre-2018 only; SUSPENDED under § 67(g) 2018+).
    pub agi_cents: u64,
}

#[derive(Debug, Clone, Serialize, PartialEq, Eq)]
pub struct Section67gResult {
    pub taxpayer_status: TaxpayerStatus,
    pub expense_category: ExpenseCategory,
    pub section_67g_suspension_applies: bool,
    pub deductible_amount_cents: u64,
    pub deduction_pathway: &'static str,
    pub permanent_after_obbba: bool,
    pub failure_reasons: Vec<String>,
    pub citation: &'static str,
    pub notes: Vec<String>,
}

pub fn check(input: &Section67gInput) -> Section67gResult {
    let mut failure_reasons: Vec<String> = Vec::new();

    let post_tcja = input.tax_year >= 2018;
    let permanent_after_obbba = input.tax_year >= 2026;

    let escapes_suspension = match input.expense_category {
        ExpenseCategory::Section67bExemptItem => true,
        ExpenseCategory::Section67eEstateAdminExpense => {
            matches!(
                input.taxpayer_status,
                TaxpayerStatus::EstateOrNonGrantorTrust
            )
        }
        ExpenseCategory::Section162TradeOrBusinessExpense => true,
        ExpenseCategory::Section212InvestmentExpense => matches!(
            input.taxpayer_status,
            TaxpayerStatus::TraderWithSection475fElection | TaxpayerStatus::HedgeFundLpTraderFund
        ),
        ExpenseCategory::UnreimbursedEmployeeBusinessExpense => false,
        ExpenseCategory::TaxReturnPreparationFee => false,
        ExpenseCategory::HobbyLoss => false,
    };

    let section_67g_suspension_applies = post_tcja && !escapes_suspension;

    let deductible_amount_cents = if section_67g_suspension_applies {
        0
    } else {
        input.expense_amount_cents
    };

    let deduction_pathway = match (input.expense_category, escapes_suspension) {
        (ExpenseCategory::Section67bExemptItem, _) => "§ 67(b) exempt — itemized deduction allowed",
        (ExpenseCategory::Section67eEstateAdminExpense, true) => {
            "§ 67(e) estate/trust carveout — administration expense allowed"
        }
        (ExpenseCategory::Section162TradeOrBusinessExpense, _) => {
            "§ 162 trade or business deduction (Schedule C)"
        }
        (ExpenseCategory::Section212InvestmentExpense, true) => {
            "§ 475(f) trader status converts § 212 to § 162 (Schedule C)"
        }
        (ExpenseCategory::Section212InvestmentExpense, false) => {
            "§ 67(g) suspension — § 212 investment expense NON-DEDUCTIBLE"
        }
        (ExpenseCategory::UnreimbursedEmployeeBusinessExpense, _) => {
            "§ 67(g) suspension — unreimbursed employee expense NON-DEDUCTIBLE"
        }
        (ExpenseCategory::TaxReturnPreparationFee, _) => {
            "§ 67(g) suspension — tax preparation fee NON-DEDUCTIBLE"
        }
        (ExpenseCategory::HobbyLoss, _) => "§ 67(g) suspension + § 183 hobby loss NON-DEDUCTIBLE",
        _ => "general suspension",
    };

    if section_67g_suspension_applies && input.expense_amount_cents > 0 {
        failure_reasons.push(format!(
            "26 USC § 67(g) — miscellaneous itemized deduction NOT ALLOWED for taxable year beginning after December 31, 2017; {} cents NON-DEDUCTIBLE; OBBBA 2025 made § 67(g) suspension PERMANENT",
            input.expense_amount_cents
        ));
    }

    if matches!(
        input.taxpayer_status,
        TaxpayerStatus::InvestorNoTraderStatus | TaxpayerStatus::HedgeFundLpInvestorFund
    ) && matches!(
        input.expense_category,
        ExpenseCategory::Section212InvestmentExpense
    ) && post_tcja
    {
        failure_reasons.push(
            "ESCAPE VALVE: 26 USC § 475(f) — individual investor may elect TRADER STATUS to convert § 212 investment expenses into § 162 trade-or-business deductions on Schedule C; § 475(f) election ALSO converts trading gain/loss to ordinary mark-to-market + exempts trader business income from § 1411 NIIT 3.8% surtax".to_string(),
        );
    }

    if matches!(
        input.taxpayer_status,
        TaxpayerStatus::EstateOrNonGrantorTrust
    ) && !matches!(
        input.expense_category,
        ExpenseCategory::Section67eEstateAdminExpense
    ) && post_tcja
    {
        failure_reasons.push(
            "26 USC § 67(e) + Treas. Reg. § 1.67-4 + IRS Notice 2018-61 — estate or non-grantor trust may deduct ONLY (1) costs paid or incurred in connection with administration of estate/trust which would not have been incurred if property were not held in such trust or estate; (2) distributions to beneficiaries; (3) distributable net income computations".to_string(),
        );
    }

    let notes: Vec<String> = vec![
        "26 USC § 67(g) — notwithstanding subsection (a), NO MISCELLANEOUS ITEMIZED DEDUCTION shall be allowed for any taxable year beginning AFTER DECEMBER 31, 2017".to_string(),
        "Enacted by Tax Cuts and Jobs Act of 2017 § 11045 (Pub. L. 115-97, December 22, 2017); originally scheduled to sunset after December 31, 2025".to_string(),
        "One Big Beautiful Bill Act of 2025 (OBBBA, H.R. 1, signed July 4, 2025) made § 67(g) suspension PERMANENT — no further sunset".to_string(),
        "26 USC § 67(b)(1)-(12) — twelve categories of itemized deductions NOT treated as miscellaneous and therefore NOT suspended by § 67(g): (1) § 163 interest; (2) § 164 taxes; (3) § 165(a) casualty losses; (4) § 170 charitable contributions; (5) § 213 medical expenses; (6) § 691(c) IRD; (7) § 215 alimony; (8) § 217 moving (armed forces only); (9) § 1341 claim of right; (10) gambling losses; (11) § 642(c) charity for estates/trusts; (12) § 7702B(a)(2) qualified long-term care insurance".to_string(),
        "26 USC § 67(e) + Treas. Reg. § 1.67-4 + IRS Notice 2018-61 — ESTATES AND TRUSTS CARVEOUT — § 67(g) does NOT apply to: (1) costs paid or incurred in connection with administration of estate/trust which would not have been incurred if property were not held in such trust/estate; (2) distributions to beneficiaries under § 651, § 661, § 662; (3) distributable net income computations".to_string(),
        "26 USC § 475(f) — TRADER ELECTION is the single most important ESCAPE VALVE from § 67(g) suspension; converts § 212 investment expenses into § 162 trade-or-business deductions on Schedule C; ALSO converts trading gain/loss to ordinary mark-to-market; ALSO exempts trader business income from § 1411 NIIT 3.8% surtax".to_string(),
        "Items SUSPENDED by § 67(g) — unreimbursed employee business expenses (Form 2106); investment advisory fees; investment custody/safekeeping fees; financial publication subscriptions; investment-related professional fees; home office (employee or investor); trading platform subscriptions (non-trader status); travel to investment seminars; tax-return preparation fees (individual); hobby losses; safe deposit box fees; § 212(1) production-of-income; § 212(2) management/conservation/maintenance; § 212(3) tax determination/collection expenses".to_string(),
        "Trader-critical fact patterns: (1) Investor WITHOUT § 475(f) trader status — ALL investment expenses NON-DEDUCTIBLE; (2) Trader WITH § 475(f) trader status — SAME expenses fully deductible as § 162 on Schedule C; (3) Hedge fund LP investor in NON-TRADER fund — K-1 § 212 expenses pass through as suspended misc itemized; (4) Hedge fund LP investor in TRADER-STATUS fund — K-1 § 162 expenses pass through as ordinary deductions; (5) Estate or non-grantor trust — § 67(e) carveout preserves administration expenses".to_string(),
        "PFIC fund expense interaction — QEF (§ 1295) ordinary inclusion + investment expenses both treated as § 212 → expenses NON-DEDUCTIBLE under § 67(g) absent trader status; mark-to-market (§ 1296) inclusion ordinary character does NOT alter § 67(g) suspension of expense deduction".to_string(),
        "§ 1411 NIIT interaction — § 67(g) suspension and § 1411 3.8% surtax compound: investor with non-trader status pays full marginal rate on investment income + 3.8% NIIT WITHOUT being able to deduct investment expenses; trader status escapes BOTH § 67(g) suspension AND § 1411 NIIT (trade-or-business income is exempt)".to_string(),
    ];

    Section67gResult {
        taxpayer_status: input.taxpayer_status,
        expense_category: input.expense_category,
        section_67g_suspension_applies,
        deductible_amount_cents,
        deduction_pathway,
        permanent_after_obbba,
        failure_reasons,
        citation: "26 USC § 67(a)-(g); Tax Cuts and Jobs Act of 2017 § 11045 (Pub. L. 115-97, December 22, 2017); One Big Beautiful Bill Act of 2025 (H.R. 1, signed July 4, 2025); 26 USC § 162; 26 USC § 212; 26 USC § 67(b)(1)-(12); 26 USC § 67(e); 26 USC § 475(f); 26 USC § 1411; Treas. Reg. § 1.67-4; IRS Notice 2018-61",
        notes,
    }
}

