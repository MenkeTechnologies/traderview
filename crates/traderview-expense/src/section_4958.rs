        let mut i = baseline();
        i.rebuttable_presumption_advance_approval = true;
        i.rebuttable_presumption_comparability_data = true;
        i.rebuttable_presumption_contemporaneous_documentation = true;
        let out = check(&i);
        let joined = out.notes.join(" ");
        assert!(joined.contains("burden of proof shifted to IRS"));
