//! Multi-jurisdictional rental property RADON DISCLOSURE
//! and MITIGATION compliance framework. When a landlord
//! rents a residential property, what radon-hazard
//! disclosure must be provided to tenant at lease
//! signing, what tenant testing rights attach, what
//! mitigation obligations follow elevated test results,
//! and what failure-mode liabilities expose landlord
//! after a tenant develops lung cancer with multi-decade
//! latency? Radon is the #2 LEADING CAUSE OF LUNG CANCER
//! in the United States per EPA, killing approximately
//! 21,000 Americans annually.
//!
//! Distinct from sibling modules: rental_lead_pipe_
//! disclosure (lead service lines), rental_basement_water_
//! intrusion_disclosure (water intrusion), rental_chimney_
//! fireplace_inspection_disclosure (iter 471 — chimney
//! safety), rental_carbon_monoxide_detector (CO sensor),
//! rental_natural_gas_leak_response (iter 485 — methane
//! leak), rental_underground_storage_tank_disclosure
//! (UST/LUST), tenant_emotional_distress_damages.
//!
//! Three-jurisdiction framework:
//!
//! 1. ILLINOIS (most stringent for tenants) — Illinois
//!    Tenants Radon Protection Act, 765 ILCS 90/, signed
//!    by Governor Pritzker on July 28, 2023 (effective
//!    January 1, 2024). Pre-lease disclosure: landlord
//!    must provide tenant the state IEMA-OHS approved
//!    Radon Pamphlet PLUS any records indicating a radon
//!    hazard in the dwelling. TENANT 90-DAY TESTING
//!    WINDOW from commencement of lease — tenant may
//!    conduct radon test at own expense. If tenant test
//!    result exceeds 4 pCi/L IEMA-OHS RECOMMENDED ACTION
//!    LEVEL AND landlord ELECTS NOT TO MITIGATE, tenant
//!    may TERMINATE THE LEASE. Statute does NOT obligate
//!    landlord to conduct testing or mitigation at
//!    landlord cost — only to disclose and respond.
//! 2. MAINE — 10 M.R.S. § 1494-A et seq. Maine Radon
//!    Disclosure law: landlord-rented residential
//!    properties must test for radon every 10 years and
//!    provide written disclosure of test results to
//!    tenant; failure to test or disclose is a violation
//!    of Maine residential landlord-tenant act with civil
//!    penalty. Maine action level: 4 pCi/L per EPA
//!    guidance + state-specific recommendations to
//!    consider reducing levels between 2 and 4 pCi/L.
//! 3. DEFAULT — Federal EPA 4 pCi/L action level
//!    recommendation (US EPA, A Citizen's Guide to
//!    Radon, EPA 402/K-09/001); EPA Map of Radon Zones
//!    classifies counties as Zone 1 (highest, predicted
//!    indoor radon average > 4 pCi/L) / Zone 2
//!    (moderate, 2-4 pCi/L) / Zone 3 (low, < 2 pCi/L);
//!    common-law implied warranty of habitability per
//!    Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984);
//!    Green v. Superior Court, 10 Cal. 3d 616 (1974);
//!    Cal. Civ. Code § 1941.1 implied warranty of
//!    sanitary facilities.
//!
//! Radon hazard data per US EPA:
//! 1. Radon is the #2 LEADING CAUSE OF LUNG CANCER in
//!    the US (after smoking); approximately 21,000 deaths
//!    annually
//! 2. EPA estimates 1 in 15 homes nationally have
//!    elevated radon levels > 4 pCi/L
//! 3. EPA recommends fixing homes at 4 pCi/L or higher
//!    and considering fixes between 2-4 pCi/L
//! 4. Elevated radon levels can be reduced to 4 pCi/L or
//!    less 95% of the time via mitigation
//! 5. Mitigation cost typically $800-$1,500 for active
//!    soil depressurization (ASD) system installed by
//!    AARST-NRPP certified contractor
//!
//! Mitigation methodology — AARST-NRPP certified
//! contractor required for warranty coverage:
//! 1. ACTIVE SOIL DEPRESSURIZATION (ASD) — most common;
//!    PVC pipe with inline fan extracts radon-laden soil
//!    gas from beneath slab and vents above roofline;
//!    $800-$1,500 typical install
//! 2. SUB-SLAB DEPRESSURIZATION (SSD) — variant for
//!    basements with poured concrete floor
//! 3. SUB-MEMBRANE DEPRESSURIZATION — for crawl spaces;
//!    polyethylene membrane sealed to walls with fan
//!    suction beneath
//! 4. SEALING CRACKS in foundation slab + sump cover +
//!    pipe penetrations (passive component only — rarely
//!    sufficient alone)
//! 5. POST-MITIGATION verification testing required to
//!    confirm < 4 pCi/L after system operation
//!
//! Universal five failure-mode liability framework:
//! 1. FAILURE TO PROVIDE RADON PAMPHLET at lease signing
//!    → Illinois 765 ILCS 90 statutory violation +
//!    tenant rescission claim + civil penalty
//! 2. FAILURE TO DISCLOSE KNOWN ELEVATED RADON LEVELS →
//!    common-law fraud + tort negligence + state
//!    consumer-protection statute + punitive damages
//!    exposure
//! 3. DECLINE TO MITIGATE AFTER TENANT TEST > 4 pCi/L →
//!    Illinois 765 ILCS 90 tenant 90-day LEASE
//!    TERMINATION right + tenant relocation cost
//!    recovery
//! 4. FAILURE TO ENGAGE AARST-NRPP CERTIFIED CONTRACTOR
//!    → ineffective remediation + recurring liability +
//!    insurance denial
//! 5. LUNG CANCER CLAIM (multi-decade latency) → tort
//!    negligence + wrongful death + punitive damages +
//!    IIED parallel to tenant_emotional_distress_damages
//!    iter 453; settlements routinely exceed $1M-$5M
//!
//! Trader-landlord critical because (1) Illinois 765
//! ILCS 90 effective January 1, 2024 is the leading
//! state-level radon disclosure regime — pamphlet
//! delivery at lease signing is the single most
//! important compliance task; (2) tenant 90-day
//! testing right combined with lease-termination right
//! creates significant turnover risk for landlords who
//! decline to mitigate; (3) Illinois Zone 1 counties
//! (Cook, DuPage, Kane, Lake, McHenry, Will, Winnebago,
//! and many central + southern IL counties) average
//! ABOVE 4 pCi/L — making affected properties high-
//! risk for tenant action; (4) AARST-NRPP certified
//! mitigation cost $800-$1,500 is trivial relative to
//! $1M-$5M+ lung cancer litigation exposure; (5)
//! Maine, Florida, New Jersey, Iowa, Minnesota have
//! growing state-level radon disclosure regimes
//! following EPA Radon Action Plan.
//!
//! Authority: Illinois Tenants Radon Protection Act,
//! 765 ILCS 90/ (effective January 1, 2024); 10 M.R.S.
//! § 1494-A et seq. (Maine radon disclosure); US EPA,
//! A Citizen's Guide to Radon, EPA 402/K-09/001; US EPA
//! Map of Radon Zones, EPA 402-R-93-071; US EPA action
//! level 4 pCi/L; AARST-NRPP National Radon Proficiency
//! Program; Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984);
//! Green v. Superior Court, 10 Cal. 3d 616 (1974); Cal.
//! Civ. Code § 1941.1.

use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Copy, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum Jurisdiction {
    Illinois,
    Maine,
    Default,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum EpaRadonZone {
    Zone1HighRisk,     // > 4 pCi/L predicted average
    Zone2ModerateRisk, // 2-4 pCi/L predicted average
    Zone3LowRisk,      // < 2 pCi/L predicted average
    Unknown,
}

#[derive(Debug, Clone, Deserialize, Serialize)]
pub struct Input {
    pub jurisdiction: Jurisdiction,
    pub epa_radon_zone: EpaRadonZone,
    pub iema_pamphlet_provided_at_lease: bool,
    pub known_radon_hazard_records_disclosed: bool,
    pub maine_10_year_test_completed: bool,
    pub maine_test_result_disclosed_to_tenant: bool,
    pub tenant_conducted_radon_test: bool,
    pub tenant_test_result_pci_per_liter_tenths: u32, // pCi/L × 10 (e.g., 45 = 4.5 pCi/L)
    pub landlord_elected_to_mitigate: bool,
    pub aarst_nrpp_certified_contractor_engaged: bool,
    pub post_mitigation_verification_below_4_pci: bool,
    pub tenant_lung_cancer_claim_reported: bool,
}

#[derive(Debug, Clone, PartialEq, Eq, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum Severity {
    Compliant,
    PamphletMissing,
    KnownHazardUndisclosed,
    MaineTestOrDisclosureMissing,
    MitigationDeclinedTenantTerminationRight,
    UncertifiedContractorEngaged,
    LungCancerClaim,
}

#[derive(Debug, Clone, Deserialize, Serialize)]
pub struct Output {
    pub severity: Severity,
    pub jurisdiction_specific_actions: Vec<String>,
    pub notes: Vec<String>,
}

pub const ACTION_LEVEL_TENTHS: u32 = 40; // 4.0 pCi/L expressed as tenths
pub const MAINE_TEST_CYCLE_YEARS: u32 = 10;

pub type RentalRadonMitigationDisclosureInput = Input;
pub type RentalRadonMitigationDisclosureResult = Output;

pub fn check(input: &Input) -> Output {
    let notes: Vec<String> = vec![
        "Three-jurisdiction framework: Illinois (most stringent for tenants — Illinois Tenants Radon Protection Act, 765 ILCS 90/ effective January 1, 2024; landlord must provide IEMA-OHS Radon Pamphlet + known-hazard records at lease signing; tenant 90-DAY TESTING WINDOW + 4 pCi/L action level + tenant LEASE TERMINATION right if landlord declines to mitigate); Maine (10 M.R.S. § 1494-A et seq. — landlord must test every 10 years + disclose results); Default (EPA 4 pCi/L action level + EPA Map of Radon Zones Zone 1/2/3 + common-law implied warranty per Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984) + Green v. Superior Court, 10 Cal. 3d 616 (1974) + Cal. Civ. Code § 1941.1).".to_string(),
        "Radon hazard data per US EPA: #2 LEADING CAUSE OF LUNG CANCER (after smoking) killing approximately 21,000 Americans annually; 1 in 15 homes nationally have elevated radon levels > 4 pCi/L; EPA recommends fixing at 4 pCi/L+ and considering fixes 2-4 pCi/L; elevated levels can be reduced to < 4 pCi/L 95% of the time via mitigation.".to_string(),
        "Mitigation methodology — AARST-NRPP certified contractor required: (1) ACTIVE SOIL DEPRESSURIZATION (ASD) — most common, $800-$1,500 typical install; (2) SUB-SLAB DEPRESSURIZATION (SSD) for basements; (3) SUB-MEMBRANE DEPRESSURIZATION for crawl spaces; (4) sealing cracks in slab + sump cover + pipe penetrations (passive only — rarely sufficient alone); (5) POST-MITIGATION verification testing required to confirm < 4 pCi/L.".to_string(),
        "Five universal failure-mode liabilities: (1) FAILURE TO PROVIDE RADON PAMPHLET at lease signing → Illinois 765 ILCS 90 statutory violation + tenant rescission + civil penalty; (2) FAILURE TO DISCLOSE KNOWN ELEVATED RADON → common-law fraud + tort negligence + state consumer-protection + punitive damages; (3) DECLINE TO MITIGATE after tenant test > 4 pCi/L → Illinois 90-day LEASE TERMINATION right + tenant relocation cost recovery; (4) FAILURE TO ENGAGE AARST-NRPP CERTIFIED CONTRACTOR → ineffective remediation + recurring liability + insurance denial; (5) LUNG CANCER CLAIM (multi-decade latency) → tort negligence + wrongful death + punitive damages + $1M-$5M+ settlement routine.".to_string(),
        "EPA Map of Radon Zones classifies counties: Zone 1 (HIGHEST, predicted indoor radon average > 4 pCi/L — including Cook + DuPage + Kane + Lake + McHenry + Will + Winnebago and many central + southern IL counties); Zone 2 (moderate 2-4 pCi/L); Zone 3 (lowest < 2 pCi/L). Maine + Iowa + Minnesota + parts of MA + PA + WI + WV are predominantly Zone 1.".to_string(),
        "Companion modules: rental_lead_pipe_disclosure (lead service lines), rental_basement_water_intrusion_disclosure, rental_chimney_fireplace_inspection_disclosure (iter 471 — chimney safety), rental_carbon_monoxide_detector (CO sensor), rental_natural_gas_leak_response (iter 485 — methane leak), rental_underground_storage_tank_disclosure (UST/LUST), tenant_emotional_distress_damages.".to_string(),
    ];
    let mut actions: Vec<String> = Vec::new();

    if input.tenant_lung_cancer_claim_reported {
        actions.push("Tenant lung cancer claim reported: engage counsel + preserve evidence including all radon disclosures and test results + insurance carrier notification; tort negligence + wrongful death + punitive damages + IIED parallel to tenant_emotional_distress_damages iter 453; multi-decade radon-exposure latency complicates causation but $1M-$5M+ settlements routine.".to_string());
    }

    let elevated_tenant_test = input.tenant_conducted_radon_test
        && input.tenant_test_result_pci_per_liter_tenths > ACTION_LEVEL_TENTHS;
    if elevated_tenant_test && !input.landlord_elected_to_mitigate {
        actions.push(format!(
            "Tenant test result {} pCi/L exceeds 4.0 pCi/L EPA action level and landlord ELECTED NOT TO MITIGATE: Illinois 765 ILCS 90 grants tenant LEASE TERMINATION right within 90-day window; tenant relocation cost recovery may apply.",
            input.tenant_test_result_pci_per_liter_tenths / 10
        ));
    }

    if input.landlord_elected_to_mitigate && !input.aarst_nrpp_certified_contractor_engaged {
        actions.push("Mitigation engaged WITHOUT AARST-NRPP certified contractor: ineffective remediation + warranty void + insurance denial; engage National Radon Proficiency Program certified contractor for active soil depressurization (ASD) install.".to_string());
    }

    if input.landlord_elected_to_mitigate
        && input.aarst_nrpp_certified_contractor_engaged
        && !input.post_mitigation_verification_below_4_pci
    {
        actions.push("Post-mitigation verification testing not completed: required to confirm < 4 pCi/L after system operation; tenant may demand re-mitigation if levels remain elevated.".to_string());
    }

    match input.jurisdiction {
        Jurisdiction::Illinois => {
            if !input.iema_pamphlet_provided_at_lease {
                actions.push("Illinois IEMA-OHS Radon Pamphlet NOT provided at lease signing: 765 ILCS 90 statutory violation; pamphlet must be provided BEFORE lease is signed; civil penalty + tenant rescission exposure.".to_string());
            }
            if !input.known_radon_hazard_records_disclosed {
                actions.push("Known radon hazard records NOT disclosed: 765 ILCS 90 requires landlord to provide any records indicating a radon hazard in the dwelling; fraud + tort negligence exposure if landlord had knowledge.".to_string());
            }
            actions.push("Illinois: Illinois Tenants Radon Protection Act, 765 ILCS 90/ effective January 1, 2024; pamphlet + known-records disclosure at lease + tenant 90-day testing window + 4 pCi/L IEMA-OHS action level + tenant lease-termination right.".to_string());
        }
        Jurisdiction::Maine => {
            if !input.maine_10_year_test_completed {
                actions.push("Maine 10-year radon test NOT completed: 10 M.R.S. § 1494-A et seq. requires landlord-rented residential properties to test every 10 years; civil penalty + tenant remedies.".to_string());
            }
            if input.maine_10_year_test_completed && !input.maine_test_result_disclosed_to_tenant {
                actions.push("Maine 10-year radon test completed but RESULTS NOT DISCLOSED to tenant: 10 M.R.S. § 1494-A et seq. requires written disclosure of test results.".to_string());
            }
            actions.push("Maine: 10 M.R.S. § 1494-A et seq. landlord-rented residential properties must test for radon every 10 years and provide written disclosure of results; 4 pCi/L action level per EPA + state recommendations to consider reducing 2-4 pCi/L levels.".to_string());
        }
        Jurisdiction::Default => {
            actions.push("Default jurisdiction: US EPA 4 pCi/L action level + EPA Map of Radon Zones (Zone 1 highest > 4 pCi/L / Zone 2 moderate 2-4 / Zone 3 lowest < 2) + common-law implied warranty per Hilder v. St. Peter, 478 A.2d 202 (Vt. 1984) + Cal. Civ. Code § 1941.1.".to_string());
        }
    }

    if matches!(input.epa_radon_zone, EpaRadonZone::Zone1HighRisk) {
        actions.push("Property in EPA Zone 1 (predicted indoor radon average > 4 pCi/L): heightened mitigation expectation + heightened liability exposure; baseline testing recommended even if not statutorily required.".to_string());
    }

    let il_pamphlet_missing = matches!(input.jurisdiction, Jurisdiction::Illinois)
        && !input.iema_pamphlet_provided_at_lease;
    let il_known_hazard_undisclosed = matches!(input.jurisdiction, Jurisdiction::Illinois)
        && !input.known_radon_hazard_records_disclosed;
    let me_test_or_disclosure_missing = matches!(input.jurisdiction, Jurisdiction::Maine)
        && (!input.maine_10_year_test_completed || !input.maine_test_result_disclosed_to_tenant);

    let severity = if input.tenant_lung_cancer_claim_reported {
        Severity::LungCancerClaim
    } else if input.landlord_elected_to_mitigate && !input.aarst_nrpp_certified_contractor_engaged {
        Severity::UncertifiedContractorEngaged
    } else if elevated_tenant_test && !input.landlord_elected_to_mitigate {
        Severity::MitigationDeclinedTenantTerminationRight
    } else if il_known_hazard_undisclosed {
        Severity::KnownHazardUndisclosed
    } else if il_pamphlet_missing {
        Severity::PamphletMissing
    } else if me_test_or_disclosure_missing {
        Severity::MaineTestOrDisclosureMissing
    } else {
        Severity::Compliant
    };

    Output {
        severity,
        jurisdiction_specific_actions: actions,
        notes,
    }
}

