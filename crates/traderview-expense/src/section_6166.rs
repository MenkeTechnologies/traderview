    #[test]
    fn truth_table_eligibility_severity_cells() {
        // 1. Non-resident alien → NotEligibleNonResidentAlien
        let c1 = check(&Input {
            decedent_residency: DecedentResidency::NonResidentAlien,
            ..baseline()
        });
        assert_eq!(c1.severity, Severity::NotEligibleNonResidentAlien);

        // 2. No qualifying interest
        let c2 = check(&Input {
            business_interest_type: BusinessInterestType::NoQualifyingInterest,
            ..baseline()
        });
        assert_eq!(c2.severity, Severity::NotEligibleNoQualifyingInterest);

        // 3. Below 35% threshold
        let c3 = check(&Input {
            closely_held_business_value_cents: 30_000_000_00,
            ..baseline()
        });
        assert_eq!(
            c3.severity,
            Severity::NotEligibleBelowThirtyFivePercentThreshold
        );

        // 4. Partnership fails 20%/45
        let c4 = check(&Input {
            business_interest_type: BusinessInterestType::Partnership,
            partnership_capital_interest_basis_points: 1000,
            partnership_partner_count: 100,
            ..baseline()
        });
        assert_eq!(
            c4.severity,
            Severity::NotEligiblePartnershipFailsTwentyPercentOrFortyFivePartnerTest
        );

        // 5. Corporation fails 20%/45
        let c5 = check(&Input {
            business_interest_type: BusinessInterestType::Corporation,
            corporation_voting_stock_basis_points: 1000,
            corporation_shareholder_count: 100,
            ..baseline()
        });
        assert_eq!(
            c5.severity,
            Severity::NotEligibleCorporationFailsTwentyPercentOrFortyFiveShareholderTest
        );

        // 6. Election not filed
        let c6 = check(&Input {
            election_filed_with_timely_form_706: false,
            ..baseline()
        });
        assert_eq!(c6.severity, Severity::NotEligibleElectionNotTimelyFiled);

        // 7. Active deferral
        let c7 = check(&baseline());
        assert_eq!(c7.severity, Severity::ElectionValidActiveDeferral);
    }

