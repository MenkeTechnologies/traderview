    #[test]
    fn realistic_50m_family_business_estate() {
        // Founder estate: $80M total, $60M closely-held S-corp, $20M other
        // AGEE = $80M - $5M deductions = $75M; closely-held $60M / $75M = 80%
        // Federal estate tax on $80M minus exemption (~$13.61M 2024) ≈ $26M
        // § 6166 portion = $26M × ($60M/$75M) = $20.8M deferred
        let i = Input {
            decedent_residency: DecedentResidency::UsCitizenOrResident,
            business_interest_type: BusinessInterestType::Corporation,
            closely_held_business_value_cents: 60_000_000_00,
            adjusted_gross_estate_cents: 75_000_000_00,
            partnership_capital_interest_basis_points: 0,
            partnership_partner_count: 0,
            corporation_voting_stock_basis_points: 8000, // 80%
            corporation_shareholder_count: 5,
            election_filed_with_timely_form_706: true,
            disposition_50_percent_or_more: false,
            withdrawal_50_percent_or_more: false,
            installment_payment_overdue_more_than_6_months: false,
            deferred_estate_tax_cents: 20_800_000_00,
        };
        let out = check(&i);
        assert_eq!(out.severity, Severity::ElectionValidActiveDeferral);
        assert_eq!(out.adjusted_gross_estate_percentage_basis_points, 8000);
    }

